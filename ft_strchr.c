#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && str[i] != (char)c)
	{
		i++;
	}
	if ((char)c == str[i])
		return ((char *)(str + i));
	return (0);
}
